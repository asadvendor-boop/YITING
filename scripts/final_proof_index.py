def build_final_proof_index(
    *,
    public_url: str,
    hero_incident_id: str,
    artifact_dir: Path = DEFAULT_ARTIFACT_DIR,
    hero_evidence_json: Path = DEFAULT_HERO_EVIDENCE,
    output_md: Path = DEFAULT_OUTPUT,
    fetch_evidence: bool = True,
) -> Path:
    public_url = public_url.strip().rstrip("/")
    hero_incident_id = hero_incident_id.strip()
    if not public_url.startswith("https://"):
        raise FinalProofError("--public-url must be an absolute https:// URL")
    if not hero_incident_id:
        raise FinalProofError("--hero-incident-id is required")

    qwen = _read_json(artifact_dir / "qwen-smoke.json")
    baseline = _read_json(artifact_dir / "track3-baseline.json")
    deployment = _read_json(artifact_dir / "deployment-verification.json")
    backup_restore = _read_json(artifact_dir / "live" / "backup-restore.json")
    if fetch_evidence:
        evidence = _fetch_json(f"{public_url}/evidence/{hero_incident_id}")
        hero_evidence_json.parent.mkdir(parents=True, exist_ok=True)
        hero_evidence_json.write_text(
            json.dumps(evidence, indent=2, sort_keys=True) + "\n",
            encoding="utf-8",
        )
    else:
        evidence = _read_json(hero_evidence_json)

    _validate_inputs(
        qwen=qwen,
        baseline=baseline,
        deployment=deployment,
        backup_restore=backup_restore,
        evidence=evidence,
        hero_incident_id=hero_incident_id,
    )
    output_md.parent.mkdir(parents=True, exist_ok=True)
    output_md.write_text(
        render_index(
            public_url=public_url,
            hero_incident_id=hero_incident_id,
            qwen=qwen,
            baseline=baseline,
            deployment=deployment,
            backup_restore=backup_restore,
            evidence=evidence,
        ),
        encoding="utf-8",
    )
    return output_md


def main() -> int:
    parser = argparse.ArgumentParser(description="Build final YITING proof index")
    parser.add_argument("--public-url", default=os.getenv("PUBLIC_BASE_URL", ""))
    parser.add_argument("--hero-incident-id", default=os.getenv("HERO_INCIDENT_ID", ""))
    parser.add_argument("--artifact-dir", type=Path, default=DEFAULT_ARTIFACT_DIR)
    parser.add_argument("--hero-evidence-json", type=Path, default=DEFAULT_HERO_EVIDENCE)
    parser.add_argument("--output-md", type=Path, default=DEFAULT_OUTPUT)
    parser.add_argument(
        "--no-fetch-evidence",
        action="store_true",
        help="read --hero-evidence-json instead of fetching from the public deployment",
    )
    args = parser.parse_args()

    try:
        output = build_final_proof_index(
            public_url=args.public_url,
            hero_incident_id=args.hero_incident_id,
            artifact_dir=args.artifact_dir,
            hero_evidence_json=args.hero_evidence_json,
            output_md=args.output_md,
            fetch_evidence=not args.no_fetch_evidence,
        )
    except FinalProofError as exc:
        print(f"final proof index failed: {exc}")
        return 2
    print(f"Final proof index written: {output}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
